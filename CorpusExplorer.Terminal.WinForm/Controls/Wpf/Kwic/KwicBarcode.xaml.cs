    private void AddMouseEvents()
    {
      foreach (var rectangle in _mapping)
      {
        rectangle.Value.MouseEnter += (sender, args) =>
        {
          if (!(sender is Rectangle rect))
            return;
          if (Equals(rect.Fill, NormalColorBrush))
            rect.Fill = HighlightColorBrush;
        };
        rectangle.Value.MouseLeave += (sender, args) =>
        {
          if (!(sender is Rectangle rect))
            return;
          if (Equals(rect.Fill, HighlightColorBrush))
            rect.Fill = NormalColorBrush;
        };
      }
    }

